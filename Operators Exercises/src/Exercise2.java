
public class Exercise2 {

	public static void main(String[] args) {
		System.out.println(multiplication(12,7));
		System.out.println(subtraction(12,7));
		System.out.println(division(12,7));

	}

	public static int multiplication(int int1, int int2) {
		return int1 * int2;	
	}
	
	public static int subtraction(int int1, int int2) {
		return int1 - int2;	
	}
	
	public static double division(double int1, double int2) {
		return int1 / int2;	
	}
	
}
